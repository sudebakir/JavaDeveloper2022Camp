
public class Ders9devam {

	public static void main(String[] args) {
	
		//for
				for (int i=2; i<10; i+=2) {
				
				System.out.println(i);
				
				}

				System.out.println("For Döngüsü Bitti");

				int i=1;
        //while
				while (i<10) {
					System.out.println(i);
					i+=2;
					
				}
			
				
				System.out.println("While Döngüsü Bitti");
				
		
		

	}

}
